package ModuloGradoAcademico;

/**
 * La clase Calificaciones representa una evaluación o calificación dentro de un curso.
 * Contiene información sobre la calificación, su peso y si está activa.
 */
public class Calificaciones {
    /**
     * El código único que identifica la calificación.
     */
    private int codigo;
    /**
     * Una descripción de la calificación.
     */
    private String descripcion;
    /**
     * El peso o ponderación de la calificación en el cálculo final.
     */
    private double peso_calificacion;
    /**
     * Indica si la calificación está activa o no.
     */
    private boolean activo;
}
