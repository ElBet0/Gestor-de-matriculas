package ModuloFamilia;

/**
 * La clase TipoDeuda representa una categoría o tipo de deuda.
 * Contiene información sobre el tipo de deuda, su nombre y descripción.
 */
public class TipoDeuda {
    /**
     * El código único que identifica el tipo de deuda.
     */
    private int tipo_deuda;
    /**
     * El nombre del tipo de deuda.
     */
    private String nombre;
    /**
     * Una descripción detallada del tipo de deuda.
     */
    private String descripcion;
}
