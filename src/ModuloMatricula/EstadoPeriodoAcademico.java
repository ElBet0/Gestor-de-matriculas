package ModuloMatricula;

/**
 * La enumeración EstadoPeriodoAcademico representa los posibles estados de un período académico.
 */
public enum EstadoPeriodoAcademico {
    /**
     * El período académico ha finalizado.
     */
    FINALIZADO,
    /**
     * El período académico está actualmente en curso.
     */
    VIGENTE,
    /**
     * El período académico está programado para el futuro.
     */
    FUTURO
}
